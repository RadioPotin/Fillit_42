#include "fillit.h"

void	ft_eraser(char **map, t_tetri *node, int y, int x)
{
	int j;

	j = 0;
	while (j < 4)
	{
		map[y + node->coor[j][0]][x + node->coor[j][1]] = '.';
		j++;
	}
}
